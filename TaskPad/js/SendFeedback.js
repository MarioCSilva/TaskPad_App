$(document).ready(function () {
    $("#myForm").css({display :"none"});
    $("#teste").click(function(){
        $("#myForm").css({display :"block"});
        $("#teste").css({display :"none"});
        });
        $("#close").click(function(){
            $("#myForm").css({display :"none"});
            $("#teste").css({display :"block"});
         });  
          
         
});    

